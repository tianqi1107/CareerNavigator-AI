def generate_sql():
    lines = []
    lines.append("USE careernavigator;")
    lines.append("SET NAMES utf8mb4;")
    lines.append("")

    # 公务员岗位
    lines.append("-- 公务员岗位")
    lines.append("INSERT INTO job (category_id, job_name, company_name, company_type, salary_min, salary_max, city, province, education_req, experience_req, skills_required, description, is_civil_service, is_state_owned, hot_score) VALUES")
    vals = []
    for j in civil_service_jobs:
        name, company, city, edu, major_req, smin, smax, hot = j
        vals.append(f"(101, '{escape(name)}', '{escape(company)}', '公务员', {smin}, {smax}, '{escape(city)}', '全国', '{escape(edu)}', '不限', '[]', '{escape(major_req)}专业可报', 1, 0, {hot})")
    lines.append(",\n".join(vals) + ";")

    lines.append("")

    # 国企/央企岗位
    lines.append("-- 国企/央企岗位")
    lines.append("INSERT INTO job (category_id, job_name, company_name, company_type, salary_min, salary_max, city, province, education_req, experience_req, skills_required, description, is_civil_service, is_state_owned, hot_score) VALUES")
    vals = []
    for j in soe_jobs:
        name, company, city, edu, major_req, smin, smax, hot = j
        cat_id = 301  # default to 国企
        vals.append(f"({cat_id}, '{escape(name)}', '{escape(company)}', '国企', {smin}, {smax}, '{escape(city)}', '全国', '{escape(edu)}', '不限', '[]', '要求{escape(major_req)}相关专业', 0, 1, {hot})")
    lines.append(",\n".join(vals) + ";")

    return "\n".join(lines)


