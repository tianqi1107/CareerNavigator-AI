def escape_sql(s):
    """Escape single quotes for SQL."""
    return str(s).replace("'", "\\'")


def generate_sql():
    lines = []
    lines.append("USE careernavigator;")
    lines.append("SET NAMES utf8mb4;")
    lines.append("SET CHARACTER SET utf8mb4;")
    lines.append("")
    lines.append("-- ============================================================")
    lines.append("-- 本科专业 (Undergraduate Majors)")
    lines.append("-- ============================================================")
    lines.append("INSERT INTO major (major_code, major_name, category, first_level, description, employment_direction, avg_salary, employment_rate, hot_score) VALUES")

    vals = []
    for m in undergraduate_majors:
        code, name, cat, fl, emp, sal, rate, hot = m
        vals.append(f"('{escape_sql(code)}', '{escape_sql(name)}', '{escape_sql(cat)}', '{escape_sql(fl)}', '{escape_sql(name)}专业', '{escape_sql(emp)}', {sal}, {rate}, {hot})")
    lines.append(",\n".join(vals) + ";")

    lines.append("")
    lines.append("-- ============================================================")
    lines.append("-- 专科专业 (Vocational Majors)")
    lines.append("-- ============================================================")
    lines.append("INSERT INTO major (major_code, major_name, category, first_level, description, employment_direction, avg_salary, employment_rate, hot_score) VALUES")

    vals = []
    for m in vocational_majors:
        code, name, cat, fl, emp, sal, rate, hot = m
        vals.append(f"('{escape_sql(code)}', '{escape_sql(name)}', '{escape_sql(cat)}', '{escape_sql(fl)}', '{escape_sql(name)}专业（专科）', '{escape_sql(emp)}', {sal}, {rate}, {hot})")
    lines.append(",\n".join(vals) + ";")

    lines.append("")
    lines.append("-- ============================================================")
    lines.append("-- 研究生一级学科 (Graduate First-level Disciplines)")
    lines.append("-- ============================================================")
    lines.append("INSERT INTO major (major_code, major_name, category, first_level, description, employment_direction, avg_salary, employment_rate, hot_score) VALUES")

    vals = []
    for m in graduate_majors:
        code, name, cat, fl, emp, sal, rate, hot = m
        vals.append(f"('{escape_sql(code)}', '{escape_sql(name)}', '{escape_sql(cat)}', '{escape_sql(fl)}', '{escape_sql(name)}（研究生一级学科）', '{escape_sql(emp)}', {sal}, {rate}, {hot})")
    lines.append(",\n".join(vals) + ";")

    return "\n".join(lines)


